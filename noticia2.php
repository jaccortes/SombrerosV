					<p class="parrafos">De la palma de caña flecha o “Gynenum Sagithatum”, se extrae la fibra vegetal con la que los Zenú elaboran diferentes 
					productos bajo el oficio de la tejeduría.Esta materia prima, propia de su región (departamentos de Córdoba y Sucre), crece en zonas húmedas
					 y pantanosas. 
					</p>
					<p class="parrafos">Además de crear artesanías con ella, esta comunidad también utiliza la caña flecha para construir sus casas y elaborar 
						las cañas de pescar. Razón por la cual, algunos investigadores le atribuyen el nombre de ﬂecha*.
					</p>
					<p class="parrafos">Con esta fibra vegetal son tejidas piezas como sombreros, bolsos, tapetes o individuales, y para elaborarlas, lo 
						primero que hacen los artesanos es “prepararla”. Este proceso comienza recolectándola en el cultivo y cortando con un machete las hojas de
						 caña flecha; a estas se les quita la parte verde y blanda de la hoja, dejando solo la vena o “nepa” (parte de la fibra que se usa en la
						  elaboración de la trenza). 
					</p>
					<img src="img\elabora2.jpg" alt="parador turistico" width="800" height="350">
					<p class="parrafos">Después se sigue con el “raspado”, que consiste en poner la nepa sobre la pierna, para rasparla con un cuchillo 
						especial, hasta dejar lisa una parte de la vena. Cuando la fibra está raspada, el artesano la seca al sol por cuatro días 
						aproximadamente, para que esta tome un color blanco o natural. Una vez las nepas están secas, se seleccionan separando las 
						fibras más claras de las manchadas y se “ripia” o divide cada vena con un cuchillo en siete u ocho tiras. 
					</p>
					<p class="parrafos">Colores como el negro y el blanco o color natural de la fibra, son una de las características de los productos 
						tejidos en caña flecha, y para reforzarlos o lograrlos, los artesanos deben teñirla. 

					Para afianzar el color blanco o natural, los artesanos cocinan la caña flecha por una hora en una olla con agua, jugo de limón y
					 caña brava o bicarbonato de sodio. Y para lograr el color negro, se cocina en una olla por dos horas con agua y hojas de plantas
					  como la bija y luego se sumerge en barro y hojas de dividivi, catorrea, jagua, concha de palma verde, hoja de manzano o ﬂor de 
					  totumo, entre otras**.
